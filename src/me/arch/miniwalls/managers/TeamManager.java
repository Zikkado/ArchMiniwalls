package me.arch.miniwalls.managers;

import java.util.ArrayList;
import java.util.List;

import org.bukkit.entity.Player;

public class TeamManager {
	
	
	private List<Player> PlayersRed = new ArrayList<>();
	private List<Player> PlayersGreen = new ArrayList<>();
	private List<Player> PlayersBlue = new ArrayList<>();
	private List<Player> PlayersYellow = new ArrayList<>();
	
	
	
	public TeamManager() {
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

	
	
	
	public List<Player> getPlayersRed() {
		return PlayersRed;
	}

	public void setPlayersRed(List<Player> playersRed) {
		PlayersRed = playersRed;
	}

	public List<Player> getPlayersGreen() {
		return PlayersGreen;
	}

	public void setPlayersGreen(List<Player> playersGreen) {
		PlayersGreen = playersGreen;
	}

	public List<Player> getPlayersBlue() {
		return PlayersBlue;
	}

	public void setPlayersBlue(List<Player> playersBlue) {
		PlayersBlue = playersBlue;
	}

	public List<Player> getPlayersYellow() {
		return PlayersYellow;
	}

	public void setPlayersYellow(List<Player> playersYellow) {
		PlayersYellow = playersYellow;
	}

}
